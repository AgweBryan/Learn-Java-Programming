public class Fibronaci {

    public static int num = 5, fid = 1, i;

    public static void main(String[] args) {
        for (i = 0; i <= num; i++) {
            fid += i - 1;

        }
        System.out.println("Fabronaci of " + num + ": " + fid);
    }

}
