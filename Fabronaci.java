public class Fabronaci {

    public static int num = 5, fad = 1, i;

    public static void main(String[] args) {
        for (i = 0; i <= num; i++) {
            fad += i - 1;

        }
        System.out.println("Fabronaci of " + num + ": " + fad);
    }

}
